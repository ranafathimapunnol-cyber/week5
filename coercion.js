

// String coercion
let a = "5" + 2;
console.log(a); 

// Number coercion
let b = "10" - 3;
console.log(b); 

let c = "6" * "2";
console.log(c); 

// Boolean coercion
let d = true + 1;
console.log(d); 

let e = false + 10;
console.log(e); 

// Equality 
console.log(5 == "5");  
console.log(5 === "5");  

// Special
console.log(null + 1);     
console.log(undefined + 1); 

console.log("Type Coercion Example End");
